package eu.europa.ejusticeportal.dss.applet.controller.cb;

import eu.europa.ejusticeportal.dss.common.exception.CodeException;
/**
 * Interface defining a class that will handle a callback event
 */
public interface CallbackHandlerDelegate {
    /**
     * Handle the callback 
     * @param xml the xml of the callback
     * @throws CodeException
     */
     void doHandle(String xml) throws CodeException;
     
     /**
      * Get a label for the handler, for logging
      * @return the label
      */
     String getLabel();
}
